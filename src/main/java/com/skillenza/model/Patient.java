package com.skillenza.model;

public class Patient {
	private String userName;
	private String password;
	private String patientName;
	private String patientEmail;
	private String patientAddress;
	private int patientPhone;
	private String patientAdharNumber;
	private String patientPanNumber;

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getPatientName() {
		return patientName;
	}

	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}

	public String getPatientEmail() {
		return patientEmail;
	}

	public void setPatientEmail(String patientEmail) {
		this.patientEmail = patientEmail;
	}

	public String getPatientAddress() {
		return patientAddress;
	}

	public void setPatientAddress(String patientAddress) {
		this.patientAddress = patientAddress;
	}

	public int getPatientPhone() {
		return patientPhone;
	}

	public void setPatientPhone(int patientPhone) {
		this.patientPhone = patientPhone;
	}

	public String getPatientAdharNumber() {
		return patientAdharNumber;
	}

	public void setPatientAdharNumber(String patientAdharNumber) {
		this.patientAdharNumber = patientAdharNumber;
	}

	public String getPatientPanNumber() {
		return patientPanNumber;
	}

	public void setPatientPanNumber(String patientPanNumber) {
		this.patientPanNumber = patientPanNumber;
	}

}
